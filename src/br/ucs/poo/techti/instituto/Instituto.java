package br.ucs.poo.techti.instituto;

public class Instituto {
	private String nome;
	private int cep;
	
	public Instituto() {
		
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCep() {
		return cep;
	}
	public void setCep(int cep) {
		this.cep = cep;
	}
}
